package enero20Poo;

public class DatosMagicos {
	private double hipotenusa; 
	private double cateto; 
	private double obtuso; 
	private double pitagoras;
	
	public double getHipotenusa() {
		return hipotenusa;
	}
	public void setHipotenusa(double hipotenusa) {
		this.hipotenusa = hipotenusa;
	}
	public double getCateto() {
		return cateto;
	}
	public void setCateto(double cateto) {
		this.cateto = cateto;
	}
	public double getObtuso() {
		return obtuso;
	}
	public void setObtuso(double obtuso) {
		this.obtuso = obtuso;
	}
	public double getPitagoras() {
		return pitagoras;
	}
	public void setPitagoras(double pitagoras) {
		this.pitagoras = pitagoras;
	} 
	
}
