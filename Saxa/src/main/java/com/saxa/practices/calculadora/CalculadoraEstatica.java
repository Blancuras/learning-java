package com.saxa.practices.calculadora;

public class CalculadoraEstatica {
	
	public static int suma(int num1, int num2) {
		return num1 + num2; 
	}
	
	public static long resta(long num1, long num2) {
		return num1 - num2; 
	}
	
	public static long multiplicacion(long num1, long num2) {
		return num1 * num2; 
	} 
	
	public static long division( long num1, long num2) {
		return num1 / num2; 
	}
	
	public static int residuo( int num1, int num2) {
		return num1 % num2; 
	}
}
